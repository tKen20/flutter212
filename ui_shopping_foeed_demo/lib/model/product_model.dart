class ProductModel {
  final String id;
  final String title;
  final String image;
  final double price;
  final String category;
  final String desc;
  final double rate;
  final String kcal;
  final String cookingTime;

  ProductModel({
    required this.id,
    required this.title,
    required this.image,
    required this.price,
    required this.category,
    required this.desc,
    required this.rate,
    required this.kcal,
    required this.cookingTime,
  });
}
